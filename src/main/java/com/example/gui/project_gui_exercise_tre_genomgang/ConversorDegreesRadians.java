public class ConversorDegreesRadians {
	//convertDegreesToRadians
	//convertRAdiansToDegrees
	
	public double convertDegreesToRadians(double value) {
		double result = Math.toRadians(value);
		return result;
	}
	
	public double convertRAdiansToDegrees(double value) {
		double result = Math.toDegrees(value);
		return result;
	}

}
